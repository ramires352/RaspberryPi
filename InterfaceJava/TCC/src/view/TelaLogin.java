    private void botaoOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoOKActionPerformed
        if("".equals(textIP.getText())){
            JOptionPane.showMessageDialog(null, "Insira o IP!");
        }
        else{
            if("".equals(textUsuario.getText())){
                JOptionPane.showMessageDialog(null, "Insira o usu√°rio!");
            }
            else{
                if("".equals(textSenha.getText())){
                    JOptionPane.showMessageDialog(null, "Insira a senha!");
                }
                else{
                    usuario = textUsuario.getText();
                    ip = textIP.getText();
                    senha = textSenha.getText();
                    porta = Integer.parseInt(textPorta.getText());
                    
                    TCC.ssh = ShellConnectionStream.builder()
                        .username(usuario)
                        .password(senha)
                        .host(ip)
                        .port(porta)
                        .build();
                    
                    try {
                        boolean conexao = TCC.ssh.connect();
                        if(conexao){
                            JOptionPane.showMessageDialog(null, "Conex√£o realizada com sucesso!");
                            new TelaPrincipal().setVisible(true);
                            this.dispose();
                        }
                        else{
                            JOptionPane.showMessageDialog(null, "Falha na conex√£o SSH!");
                        }
                    } catch (JSchException ex) {
                        JOptionPane.showMessageDialog(null, "Falha na conex√£o SSH!");
                    }
                }
            }
        }
    }//GEN-LAST:event_botaoOKActionPerformed

